// ─── Detekcja i storage ──────────────────────────────────────────────────────

const LS_KEY = 'babylog_locale'

function detectLocale() {
  // 1. localStorage
  try {
    const saved = localStorage.getItem(LS_KEY)
    if (saved === 'pl' || saved === 'en') return saved
  } catch {}
  // 2. navigator
  if (typeof navigator !== 'undefined') {
    const lang = (navigator.language || 'pl').toLowerCase()
    if (lang.startsWith('pl')) return 'pl'
  }
  // 3. fallback — jeśli nie PL to EN
  return 'en'
}

let _currentLocale = detectLocale()
const _listeners = new Set()

export function getLocale() {
  return _currentLocale
}

/**
 * Czy aktualny locale to EN? — używane do ukrywania polskiego contentu
 * jak szczepienia PSO, polskie nazwy leków, dieta BLW, polskie milestones.
 *
 * W EN użytkownik widzi tylko: informacje referencyjne o lekach + custom dodane rzeczy.
 * Built-in polski content jest ukryty dopóki nie zrobimy anglojęzycznej
 * wersji (US CDC vaccines, US FDA dose guidelines itd.).
 */
export function isEN() {
  return _currentLocale === 'en'
}

export function setLocale(locale) {
  if (locale !== 'pl' && locale !== 'en') return
  _currentLocale = locale
  try { localStorage.setItem(LS_KEY, locale) } catch {}
  _listeners.forEach(fn => fn(locale))
}

/**
 * t(key) — zwraca przetłumaczony string.
 * Jeśli klucza nie ma, zwraca sam klucz (żeby łatwo wyłapać brakujące).
 */
export function t(key, params) {
  let str = TRANSLATIONS[_currentLocale]?.[key] ?? TRANSLATIONS.pl[key] ?? key
  if (params) {
    Object.keys(params).forEach(p => {
      str = str.replace(new RegExp('\\{' + p + '\\}', 'g'), params[p])
    })
  }
  return str
}

/**
 * tPlural — wybiera klucz .one lub .many zależnie od liczby.
 * Użycie: tPlural('summary.entries', 1) → t('summary.entries.one', {count:1})
 *         tPlural('summary.entries', 5) → t('summary.entries.many', {count:5})
 *
 * Uwaga: polski ma 3 formy (1 wpis / 2-4 wpisy / 5+ wpisów), ale upraszczamy
 * do 2 bo HistorySection pokazuje sumę dzienną gdzie rozróżnienie 1 vs reszta
 * jest najważniejsze (1 karmienie vs wiele). EN i tak ma tylko 2 formy.
 */
export function tPlural(baseKey, count, extraParams = {}) {
  const suffix = count === 1 ? '.one' : '.many'
  return t(baseKey + suffix, { count, ...extraParams })
}

/**
 * useLocale() — hook reaktywny do języka.
 * Powoduje re-render gdy język się zmieni.
 */
export function useLocale() {
  const [locale, setState] = useState(_currentLocale)
  useEffect(() => {
    const listener = (l) => setState(l)
    _listeners.add(listener)
    return () => _listeners.delete(listener)
  }, [])
  return { locale, setLocale, t }
}
